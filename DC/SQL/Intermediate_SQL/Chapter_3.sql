-- Correlated Subqueries are dependent on the main query for execution
-- However, as they are evaluated in loops, they take significant time to run

SELECT 
	-- Select country ID, date, home, and away goals from match
	main.country_id,
    main.date,
    main.home_goal, 
    main.away_goal
FROM match AS main
WHERE 
	-- Filter the main query by the subquery
	(home_goal + away_goal) > 
        (SELECT AVG((sub.home_goal + sub.away_goal) * 3)
         FROM match AS sub
         -- Join the main query to the subquery in WHERE
         WHERE main.country_id = sub.country_id);
		 
		 
		 
-- Correlated subquery with multiple conditions:

SELECT 
	-- Select country ID, date, home, and away goals from match
	main.country_id,
    main.date,
    main.home_goal,
    main.away_goal
FROM match AS main
WHERE 
	-- Filter for matches with the highest number of goals scored
	(home_goal + away_goal) = 
        (SELECT MAX(sub.home_goal + sub.away_goal)
         FROM match AS sub
         WHERE main.country_id = sub.country_id
               AND main.season = sub.season);
			   

-- Nested Subqueries:

SELECT
	EXTRACT(MONTH FROM date) AS month,
	SUM(m.home_goal + m.away_goal) AS total_goals,
	SUM(m.home_goal + m.away_goal) - (SELECT AVG(goals)
FROM (SELECT
	EXTRACT(MONTH FROM date) AS month,
	SUM(home_goal + away_goal) AS goals
	FROM match GROUP BY month)) AS avg_diff
	FROM match AS m
	GROUP BY month
	

